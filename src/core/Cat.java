public class Cat {
    private String name;
    private String breed;
    private int age;

    public Cat(String name, String breed, int age) {
        this.name = name;
        this.breed = breed;
        this.age = age;
    }
    
    public String toString(){
        return "Cat(" + "name= " + name + ", breed= " + breed + ", age= " + age + ")";
    }
}
